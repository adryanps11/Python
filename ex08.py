# e milímetros

#Criando variáveis
m = float(input('Digite uma distância em metros: '))
cm = (m * 100)
mi = (m * 1000)

#Impressão de valores em milímetros e em centimetros
print('A medida em Metros = {}m \n Centímetros = {}cm \n Milímetros = {}mm'.format(m, cm, mi))



