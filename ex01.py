#Programa para fazer soma de dois valores
#OBS: O input é uma String e precisa ser convertida em um tipo primitivo
#No Exercício será usado o tipo int

#Leia o primeiro número
num1 = int(input('Digite o primeiro número: '))

#Leia o segundo número
num2 = int(input('Digite o segundo número: '))

#Soma do primeiro valor com o segundo
soma = num1 + num2

#Imprensão da soma do primeiro valor com o segundo utilizando o método format
print('A soma entre: {} + {} = {}'.format(num1,num2,soma))





